%%%-------------------------------------------------------------------
%%% @doc
%%% asset script for asset maker
%%% @end
%%%-------------------------------------------------------------------
-module(asset_script).
-export([main/1]).
%%%===================================================================
%%% API functions
%%%===================================================================
main(_) ->
    code:add_path(filename:dirname(escript:script_name()) ++ "/../../../beam/"),
    io:format("~p~n", [catch asset_maker:start(asset())]).

%%%===================================================================
%%% record data
%%%===================================================================
asset() ->
    [
        {"src/module/asset/asset.erl", asset}
    ].
