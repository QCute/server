%%%-------------------------------------------------------------------
%%% @doc
%%% event script for event maker
%%% @end
%%%-------------------------------------------------------------------
-module(event_script).
-export([main/1]).
%%%===================================================================
%%% API functions
%%%===================================================================
main(_) ->
    code:add_path(filename:dirname(escript:script_name()) ++ "/../../../beam/"),
    io:format("~p~n", [catch event_maker:start(event())]).

%%%===================================================================
%%% event data
%%%===================================================================
event() ->
    [
        {"src/module/user/user_event.erl", "include/", {user, event}, "src/module/*/*.erl"}
    ].
